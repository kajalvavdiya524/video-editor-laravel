<?php

namespace App\Domains\Auth\Models\Traits\Scope;

/**
 * Class CompanyScope.
 */
trait CompanyScope
{
    /**
     * @param $query
     *
     * @return mixed
     */
    public function scopeOnlyDeactivated($query)
    {
        return $query->whereActive(false);
    }

    /**
     * @param $query
     *
     * @return mixed
     */
    public function scopeOnlyActive($query)
    {
        return $query->whereActive(true);
    }
}
